//to play on the key A do this(using Jquery):
const audio = document.getElementById('audio');

document.addEventListener('keydown', function(e) {
  if (e.key === 'a') {
  	if (!audio.paused) {
      audio.pause();
      audio.currentTime = 0;
    } else {
    	audio.play();
    }
  }
});

